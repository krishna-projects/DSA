package com.dsa.random;

public class Utility {

    public static void printArray(int[] arr) {
        for (int j : arr) {
            System.out.print(j + " , ");
        }
    }

    public static void printArray(String[] arr) {
        for (String j : arr) {
            System.out.print(j + " , ");
        }
    }
}
